# os.path中模块
#
# ABSPATH（）将相对路径转化为绝对路径 os.path.abspath(path)
# 目录名（）获取完整路径当中的目录部分 os.path.dirname("d:/1/test")
# 基名（）获取完整路径当中的主体部分 os.path.basename("d:/1/test")
# 分裂（）将一个完整的路径切割成目录部分和主体部分 os.path.split("d:/1/test")
# 加入（）将2个路径合并成一个 os.path.join("d:/1", "test")
# 的getSize（）获取文件的大小 os.path.getsize(path)
# ISFILE（）检测是否是文件 os.path.isfile(path)
# isdir() 检测是否是文件夹 os.path.isdir(path)



